import Head from "next/head";
import styles from "@/styles/Home.module.css";
import { Notion } from "@/lib/notion";
import { Collapse, Select, Spacer, Spinner, Text } from "@geist-ui/core";
import { useEffect, useState } from "react";
import {
  TShift,
  identifyShift,
  toDate,
  toRelativeTime,
  toTime,
} from "@/lib/utils";
import { PageObjectResponse } from "@notionhq/client/build/src/api-endpoints";

export default function Home(props: { users: Array<any> }) {
  const [userId, setUserId] = useState<string>("");
  const [shifts, setShifts] = useState<Array<any>>([]);
  const [loading, setLoading] = useState<boolean>(false);

  useEffect(() => {
    if (userId) {
      setLoading(true);
      fetch(`/api/shifts?uid=${userId}`)
        .then((res) => res.json())
        .then((data) => {
          // console.log(data);
          localStorage.setItem("salit-uid", userId);
          setShifts(data.shifts);
          setLoading(false);
        });
    } else {
      setShifts([]);
    }
  }, [userId]);

  useEffect(() => {
    const uid = localStorage.getItem("salit-uid");
    if (uid) {
      setUserId(uid);
    }
  }, []);

  return (
    <>
      <Head>
        <title>×¡×œ×¢×™×ª | ×¨×©×™××ª ×©××™×¨×”</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={`${styles.main}`}>
        <Text h1>×¨×©×™××ª ×©××™×¨×”</Text>
        <Select
          placeholder="×©×"
          onChange={(e) => setUserId(e as string)}
          width="65%"
          height="40px"
          value={userId}
          clearable
        >
          {props.users.map((user) => (
            <Select.Option key={user.id} value={user.id}>
              {user.username}
            </Select.Option>
          ))}
        </Select>
        {loading ? <Loader /> : <Shifts shifts={shifts} userId={userId} />}
      </main>
    </>
  );
}

const Loader = () => {
  return (
    <>
      <Spacer h={2} />
      <Spinner scale={5} />
    </>
  );
};

const NoShifts = () => {
  return (
    <>
      <Text h1>ğŸï¸</Text>
      <Text h3>××™×Ÿ ××©××¨×•×ª</Text>
    </>
  );
};

const Shifts = (props: {
  shifts: Array<PageObjectResponse>;
  userId: string;
}) => {
  if (props.shifts.length === 0) {
    return <NoShifts />;
  }
  return (
    <Collapse.Group className={styles.shiftsContainer}>
      {props.shifts &&
        props.shifts.map((shift) => (
          <Shift
            key={shift.id}
            shift={shift}
            type={identifyShift(shift, props.userId)}
          />
        ))}
    </Collapse.Group>
  );
};

const Shift = (props: { shift: PageObjectResponse; type: TShift }) => {
  const time =
    props.shift.properties["×–××Ÿ"].type == "date" &&
    props.shift.properties["×–××Ÿ"].date;
  const subtitle =
    time &&
    `${toDate(time.start)} @ ${toTime(time.end as string)} - ${toTime(
      time.start
    )} (${toRelativeTime(time.start)})`;
  const title = `${props.type.emoji} ${props.type.name}`;
  return (
    <Collapse title={title} subtitle={subtitle}>
      <Text>{props.type.name}</Text>
    </Collapse>
  );
};

export const getServerSideProps = async () => {
  const allUsers = await new Notion().getAllUsers();
  return {
    props: {
      users: allUsers,
    },
  };
};
